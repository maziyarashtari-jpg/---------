// Helper to find closest AS568 standard O-ring based on custom dimensions
// Returns an array of search results sorted by closeness
export const findClosestMatches = (
  targetIdMm: number,
  targetCsMm: number,
  limit: number = 5
): SearchResult[] => {
  const allDimensions = getAS568Dimensions();
  const results: SearchResult[] = allDimensions.map((o) => {
    // Normalizing differences: cross-section difference and ID difference
    // Since CS is highly restricted in standards, we can penalize CS mismatch slightly higher or treat them together.
    const idDiff = Math.abs(o.idMm - targetIdMm);
    const csDiff = Math.abs(o.csMm - targetCsMm);
    
    // Weighted distance (cross section weighs more heavily because standard CS is crucial for fitting grooves)
    const deviationMm = Math.sqrt(idDiff * idDiff + (csDiff * 3) * (csDiff * 3));

    // Dynamic Squeeze Ratio calculation if used in target gland
    let idSqueezeRatio = 0;
    if (targetCsMm > 0) {
      idSqueezeRatio = Number(((1 - (o.csMm / targetCsMm)) * 100).toFixed(1));
    }
    const stretchRatio = Number(((targetIdMm - o.idMm) / o.idMm * 100).toFixed(1));

    return {
      oRing: o,
      deviationMm: Number(deviationMm.toFixed(3)),
      idSqueezeRatio,
      stretchRatio
    };
  });

  // Sort by smallest deviation
  return results.sort((a, b) => a.deviationMm - b.deviationMm).slice(0, limit);
};
